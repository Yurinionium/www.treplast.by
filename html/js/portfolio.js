const cardList = document.querySelector('.card-list__prortfolio');
const popupElement = document.querySelector('.popup');
const popupCloseButton = document.querySelector('.popup__close');
const popupImage = document.querySelector('.popup__image');
const popupCaption = document.querySelector('.popup__caption');
const popupPrevButton = document.querySelector('.popup__prev');
const popupNextButton = document.querySelector('.popup__next');
const catalogForm = document.querySelector('#catalogForm');
const resetButton = document.querySelector('.button__reset');

let currentImageIndex = 0;
let currentImages = [];

class Card {
    constructor(templateSelector) {
        this._templateSelector = templateSelector;
    }

    _getTemplate() {
        const cardElement = document
            .querySelector(this._templateSelector)
            .content
            .querySelector('.card')
            .cloneNode(true);

        return cardElement;
    }

    _handleOpenPopup(images, description) {
        currentImages = images;
        currentImageIndex = 0;
        popupImage.src = currentImages[currentImageIndex];
        popupCaption.textContent = description;
        popupElement.classList.add('popup_is-opened');

        popupPrevButton.addEventListener('click', this._showPreviousImage);
        popupNextButton.addEventListener('click', this._showNextImage);
    }

    _showPreviousImage() {
        currentImageIndex = (currentImageIndex - 1 + currentImages.length) % currentImages.length;
        popupImage.src = currentImages[currentImageIndex];
    }

    _showNextImage() {
        currentImageIndex = (currentImageIndex + 1) % currentImages.length;
        popupImage.src = currentImages[currentImageIndex];
    }

    _handleClosePopup() {
        popupImage.src = '';
        popupCaption.textContent = '';
        popupElement.classList.remove('popup_is-opened');
    }

    _setEventListeners(images, description) {
        this._element.addEventListener('click', () => {
            this._handleOpenPopup(images, description);
        });

        popupCloseButton.addEventListener('click', () => {
            this._handleClosePopup();
        });

        popupElement.addEventListener('click', (event) => {
            if (event.target === popupElement) {
                this._handleClosePopup();
            }
        });
    }
}

class DefaultCard extends Card {
    constructor(data, templateSelector) {
        super(templateSelector);
        this._title = data.title;
        this._description = data.description;
        this._images = data.images;
    }

    generateCard() {
        this._element = super._getTemplate();
        this._setEventListeners(this._images, this._description);

        this._element.querySelector('.card__image').style.backgroundImage = `url(${this._images[0]})`;
        this._element.querySelector('.card__title').textContent = this._title;

        return this._element;
    }
}

const renderElements = (catalog = itemsCatalog) => {
    cardList.innerHTML = '';
    catalog.forEach((item) => {
        const card = new DefaultCard(item, '.default-card');
        const cardElement = card.generateCard();
        cardList.append(cardElement);
    });
};

// Сохранение состояния чекбоксов в localStorage
const saveCheckboxStates = () => {
    const checkboxStates = Array.from(document.querySelectorAll('input[name="cardType"]'))
        .map(checkbox => ({ value: checkbox.value, checked: checkbox.checked }));
    localStorage.setItem('checkboxStates', JSON.stringify(checkboxStates));
};

// Загрузка состояния чекбоксов из localStorage
const loadCheckboxStates = () => {
    const checkboxStates = JSON.parse(localStorage.getItem('checkboxStates'));
    if (checkboxStates) {
        checkboxStates.forEach(state => {
            const checkbox = document.querySelector(`input[name="cardType"][value="${state.value}"]`);
            if (checkbox) {
                checkbox.checked = state.checked;
            }
        });
    }
};

catalogForm.addEventListener('change', () => {
    const selectedTypes = Array.from(document.querySelectorAll('input[name="cardType"]:checked')).map(checkbox => checkbox.value);

    if (selectedTypes.length === 0) {
        renderElements();
    } else {
        const filteredCatalog = itemsCatalog.filter(item => selectedTypes.includes(item.type));
        renderElements(filteredCatalog);
    }

    saveCheckboxStates();
});

resetButton.addEventListener('click', (e) => {
    e.preventDefault();
    catalogForm.reset();
    renderElements();
    saveCheckboxStates();
});

document.addEventListener('DOMContentLoaded', () => {
    loadCheckboxStates();
    const selectedTypes = Array.from(document.querySelectorAll('input[name="cardType"]:checked')).map(checkbox => checkbox.value);

    if (selectedTypes.length === 0) {
        renderElements();
    } else {
        const filteredCatalog = itemsCatalog.filter(item => selectedTypes.includes(item.type));
        renderElements(filteredCatalog);
    }
});